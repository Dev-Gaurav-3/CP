void print(const vector<T> &v)
{
    for (auto &x : v)
        cout << x << " ";
    cout << "\n";
}

template <typename T>
void input(vector<T> &v, int n)
{
    v.resize(n);
    for (auto &x : v)
        cin >> x;
}

void f()
{
    int n;
    cin >> n;

    int mid = (n + 1) / 2;
    cout << mid << " ";

    int l = mid - 1;
    int r = mid + 1;

    while (l >= 1 || r <= n) {
        if (r <= n) cout << r << " ";
        if (l >= 1) cout << l << " ";
        r++;
        l--;
    }
    cout<<endl;

    return ;
}

